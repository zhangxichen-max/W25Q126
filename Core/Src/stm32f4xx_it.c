/**
  * @brief This function handles UART5 global interrupt.
  */
void UART5_IRQHandler(void)
{
  /* USER CODE BEGIN UART5_IRQn 0 */

  /* USER CODE END UART5_IRQn 0 */
  HAL_UART_IRQHandler(&huart5);
  /* USER CODE BEGIN UART5_IRQn 1 */
	uint32_t temp;
	if(USART1 == huart5.Instance)//判断是否为串口1中断
	{      
		if(RESET != __HAL_UART_GET_FLAG(&huart5,UART_FLAG_IDLE))//如果为串口1
		{
			__HAL_UART_CLEAR_IDLEFLAG(&huart5);//清除中断标志
      HAL_UART_DMAStop(&huart5);//停止DMA接收
     	temp  = __HAL_DMA_GET_COUNTER(&hdma_uart5_rx);//获取DMA当前还有多少未填充
      Rx_len =  BUFFERSIZE - temp; //计算串口接收到的数据个数
      recv_end_flag = 1;
    }
   }	

  /* USER CODE END UART5_IRQn 1 */
}

